void MainGameLoopPatch() {
    // Vanilla code
    Sub_8000954_SetKeyPressWRAM();
    Sub_80007BC_SoftResetCheck();
    ++ucMainTimer;
    ++usRandomCount;

    // Custom code
    // in CD Room
    if (sGameSeq == 48 && (ucSroomCurStatus == 1 || ucSroomCurStatus == 3)) {
        // Palette change
        // Karaoke
        if (ucSroomCurStatus == 1) {
            REG_DMA3SAD = &paletteKaraoke;
        // CDs
        } else if (ucSroomCurStatus == 3) {
            // Emerald
            if (ucSroomCdRank == 0) {
                if (ucSroomCdCol == 0) {
                    REG_DMA3SAD = &paletteEmerald1;
                } else if (ucSroomCdCol == 1) {
                    REG_DMA3SAD = &paletteEmerald2;
                } else if (ucSroomCdCol == 2) {
                    REG_DMA3SAD = &paletteEmerald3;
                } else if (ucSroomCdCol == 3) {
                    REG_DMA3SAD = &paletteEmerald4;
                }
            // Ruby
            } else if (ucSroomCdRank == 1) {
                if (ucSroomCdCol == 0) {
                    REG_DMA3SAD = &paletteRuby1;
                } else if (ucSroomCdCol == 1) {
                    REG_DMA3SAD = &paletteRuby2;
                } else if (ucSroomCdCol == 2) {
                    REG_DMA3SAD = &paletteRuby3;
                } else if (ucSroomCdCol == 3) {
                    REG_DMA3SAD = &paletteRuby4;
                }
            // Topaz
            } else if (ucSroomCdRank == 2) {
                if (ucSroomCdCol == 0) {
                    REG_DMA3SAD = &paletteTopaz1;
                } else if (ucSroomCdCol == 1) {
                    REG_DMA3SAD = &paletteTopaz2;
                } else if (ucSroomCdCol == 2) {
                    REG_DMA3SAD = &paletteTopaz3;
                } else if (ucSroomCdCol == 3) {
                    REG_DMA3SAD = &paletteTopaz4;
                }
            // Sapphire
            } else if (ucSroomCdRank == 3) {
                if (ucSroomCdCol == 0) {
                    REG_DMA3SAD = &paletteSapphire1;
                } else if (ucSroomCdCol == 1) {
                    REG_DMA3SAD = &paletteSapphire2;
                } else if (ucSroomCdCol == 2) {
                    REG_DMA3SAD = &paletteSapphire3;
                } else if (ucSroomCdCol == 3) {
                    REG_DMA3SAD = &paletteSapphire4;
                }
            }
        }
        REG_DMA3DAD = (PAL_RAM + 0x300);
        REG_DMA3CNT = 0x80000010;
        while (REG_DMA3CNT & 0x80000000) ;
        REG_DMA3DAD = (PAL_RAM + 0x320);
        REG_DMA3CNT = 0x80000010;
        while (REG_DMA3CNT & 0x80000000) ;
        REG_DMA3DAD = (PAL_RAM + 0x340);
        REG_DMA3CNT = 0x80000010;
        while (REG_DMA3CNT & 0x80000000) ;
        REG_DMA3DAD = (PAL_RAM + 0x360);
        REG_DMA3CNT = 0x80000010;
        while (REG_DMA3CNT & 0x80000000) ;
    }
}
